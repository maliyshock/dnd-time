export function generateWorldName() {
  let name = starts[getRandomNum({ max: starts.length - 1 })];
  // midCount from 0 to 3
  const midCount = getRandomNum({ max: 3 });

  for (let i = 0; i < midCount; i++) {
    name += mids[getRandomNum({ max: mids.length - 1 })];
  }

  name += ends[getRandomNum({ max: ends.length - 1 })];

  return name;
}
